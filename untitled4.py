"""**Visual Analysis**"""

# 1. Distribution of Age, Thalach, Cholesterol
fig, axs = plt.subplots(1, 3, figsize=(18, 5))
sns.histplot(df['age'], kde=True, ax=axs[0], color="skyblue")
axs[0].set_title("Age Distribution")

sns.histplot(df['thalach'], kde=True, ax=axs[1], color="lightgreen")
axs[1].set_title("Max Heart Rate (thalach) Distribution")

sns.histplot(df['chol'], kde=True, ax=axs[2], color="salmon")
